package DersProgrami.domain;

public class Sinif {

	private String num;
	private int sinifMevcudu;
	
	public Sinif(String num, int sinifMevcudu) {
		this.num = num;
		this.sinifMevcudu = sinifMevcudu;
	}
	
	public String getNum() {
		return num;
	}
	public int getSinifMevcudu() {
		return sinifMevcudu;
	}
	
}
